package entities;

/**
 *
 * @author ahmed
 */
public class Client {
    private int id;
    private String nom;

    public Client(int id) {
        this.id = id;
        
    }

    public Client(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return  nom ;
    }
    
    
}
